package ReplsAllInteresting;

public class R112ForLoop10Num {

    void numbers (int times){
        for (int i=1;i<=times;i++){
            System.out.println(i);
        }
    }
    public static void main(String[]args){
        R112ForLoop10Num obj=new R112ForLoop10Num();
        obj.numbers(10);
    }
}

