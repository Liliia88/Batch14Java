package ReplsAllInteresting;

public class R72ArraysEven {
    public static void main (String[]args){
        char[] signs={'s','a','y','b','n','c','t','d','a','e','x'};
        for (char i=0;i<signs.length;i++) {
            if (i%2==0){
                System.out.print(signs[i]+"");
            }
        }
    }   }

